class Teacher:
    def setid(self,id):
        self.id=id
    def setname(self,name):
        self.name=name
    def setsalary(self,salary):
        self.salary=salary
    def setaddress(self,address):
        self.address=address


    def getid(self):
        return self.id
    def getname(self):
        return self.name
    def getsalary(self):
        return self.salary
    def getaddress(self):
        return self.address
