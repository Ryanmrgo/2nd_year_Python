# Example nested list
nested_list = [[1, 2, 3],\
    [4, 5, 6],\
    [7, 8, 9]]

# Accessing elements in the nested list
element_1 = nested_list[0]         # Access the first inner list
element_2 = nested_list[1][2]      # Access the third element of the second inner list
element_3 = nested_list[2][0]      # Access the first element of the third inner list

# Displaying the results
print("Element 1:", element_1)
print("Element 2:", element_2)
print("Element 3:", element_3)
