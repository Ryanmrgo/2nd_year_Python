class Teacher:
    def set_id(self,id):
        self.id=id
    def set_name(self,name):
        self.name=name
    def set_salary(self,salary):
        self.salary=salary
    def set_address(self,address):
        self.address=address


    def get_id(self):
        return self.id
    def get_name(self):
        return self.name
    def get_salary(self):
        return self.salary
    def get_address(self):
        return self.address
    

        
        
    
