name="\t    Sanda Pyae Sone \n   "

print(name)
print(name.lstrip())#leftstrip
print(name.rstrip())#rightstrip
print(name.strip())#fullstrip
'''
lstrip(): Removes leading (left) whitespace characters from the string.
rstrip(): Removes trailing (right) whitespace characters from the string.
strip(): Removes both leading and trailing whitespace characters from the string.
'''